    private void initDataTable(){
        DefaultTableModel t = new DefaultTableModel();
        t.addColumn("id");
        t.addColumn("Nama");
        t.addColumn("Nilai UTS");
        t.addColumn("Nilai Tugas Mandiri");
        t.addColumn("Nilai UAS");
        t.addColumn("Peringkat");
        
        HafizRamadhan_TablePenilaian.setModel(t);
        try {
            res = conn.createStatement().executeQuery("SELECT * FROM "+TablePenilaian+" ");
            while(res.next()){
                t.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("name"),
                    res.getString("uts"),
                    res.getString("tm"),
                    res.getString("uas"),
                    res.getString("grade"),
                });
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

