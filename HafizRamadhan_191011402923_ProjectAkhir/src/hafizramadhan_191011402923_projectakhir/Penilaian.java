    private void HafizRamadhan_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HafizRamadhan_EditActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(null, "Apakah kamu yakin ingin mengupdate data ini ?", "Confirmation" , JOptionPane.YES_NO_OPTION);
        if (confirm == 0){
            Double Nilai_UTS = Double.parseDouble(HafizRamadhan_NilaiUTS.getText());
            Double Nilai_Tugas_Mandiri = Double.parseDouble(HafizRamadhan_NilaiTugasMandiri.getText());
            Double Nilai_UAS = Double.parseDouble(HafizRamadhan_NilaiUAS.getText());
            Double Nilai_Rata_Rata = Nilai_UTS + Nilai_Tugas_Mandiri + Nilai_UAS / 3;

            String Grade, Keterangan;
            if (Nilai_Rata_Rata >= 80){
                Grade = "A";
            }else if(Nilai_Rata_Rata >= 70){
                Grade = "B";
            }else if(Nilai_Rata_Rata >= 60){
                Grade = "C";
            }else if(Nilai_Rata_Rata >= 50){
                Grade = "D";
            }else{
                Grade = "E";
            }
            PreparedStatement st;
            try {
                String sql = "UPDATE "+TablePenilaian+" SET name = ?, uts = ?, tm = ?, uas = ?, grade = ? WHERE id ='"+HafizRamadhan_ID.getText()+"' ";
                st = conn.prepareStatement(sql);
                st.setString(1, HafizRamadhan_NamaLengkap.getText());
                st.setString(2, HafizRamadhan_NilaiUTS.getText());
                st.setString(3, HafizRamadhan_NilaiTugasMandiri.getText());
                st.setString(4, HafizRamadhan_NilaiUAS.getText());
                st.setString(5, Grade);
                st.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil diupdate");
                initDataTable();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal diupdate, karena: " + ex);
            }
        }
    }//GEN-LAST:event_HafizRamadhan_EditActionPerformed

